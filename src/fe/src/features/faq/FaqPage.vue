      <h3 id="C5">Biosafety and Biosecurity questions</h3>
      <br />
      <p id="P14">
        <b
          >14. How will WHO ensure the laboratories engaged in this System meet
          the necessary biosafety and biosecurity requirements?</b
        >
      </p>
      <ul>
        <li>
          Because of the nature of the work and consequences inherent to this
          public health security, WHO BioHub Facilities, and entities that
          request materials from Facilities, will be required to demonstrate the
          capacity to handle BMEPP in accordance with all relevant and
          applicable WHO, international, and national regulations and standards;
          these would include regulations for the transport of infectious
          substances, biosafety, biosecurity, access and benefit-sharing laws of
          regulations, health regulations, and other applicable laws, rules,
          regulations or and standards.
        </li>

        <li>If asked for specifics:</li>
        <ul>
          <li>
            The following, among others, of the participating facilities,
            including recipients, are proposed to be evaluated commensurate with
            the assessed risks:
          </li>
          <ol>
            <li>
              Biosafety and biosecurity risk assessment capacities and
              capabilities
            </li>
            <li>Appropriate biocontainment facilities</li>
            <li>
              Biosafety manual, containing institutional policies, programmes
              and plans
            </li>
            <li>
              Biosecurity plan, outlining the security measures including
              personnel reliability where necessary
            </li>
            <li>
              Training programme and records as well as good microbiological
              practice and procedure (GMPP) and SOPs
            </li>
          </ol>
        </ul>
      </ul>
      <br />
      <p id="P15">
        <b
          >15. Does this relate in any way to the Biological Weapons Convention
          - Nuclear Threat Initiative? What are the implications, if any, for
          other international agreements such as the Biological Weapons
          Convention, and other international initiatives addressing biosafety
          and biosecurity issues?</b
        >
      </p>
      <ul>
        <li>
          The Member States of WHO and the States parties to the BWC are best
          able to address this issue.
        </li>
        <li>
          We do wish to note that the WHO BioHub facilities and participating
          laboratories would be expected, as condition for participating in the
          System, to adhere to applicable national and international norms and
          standards regarding the use of biological materials, including, as
          appropriate, the BWC.
        </li>
        <li>
          Participation in the BioHub system does not preclude in any way
          working with other technical efforts that are underway to address
          biosecurity and biosafety in research into emerging pathogens. We hope
          that other partnerships and initiatives will be complementary to the
          System and that together all efforts will contribute to a more rapid,
          reliable, and equitable access to the benefits of research that
          coincides with the spirit of BWC’s Article X.
        </li>
      </ul>
