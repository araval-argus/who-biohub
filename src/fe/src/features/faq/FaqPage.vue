      <h3 id="C4">Laboratory and Surveillance questions</h3>
      <br />
      <p id="P10"><b>10. What is being shared?</b></p>
      <ul>
        <li>
          Clinical samples, specimens, isolates, and cultures, either original
          or processed, of a novel pathogen with epidemic or pandemic potential.
          This term does not include influenza viruses or other non-BMEPP
          pathogens that may be contained in clinical samples or specimens
          shared under the WHO BioHub System.
        </li>
        <li>
          Clinical samples and specimens mean materials taken from humans or
          animals, in as far as the samples taken from animals are shared by
          originating countries/laboratories with the WHO BioHub Facility. These
          include specimens collected from the respiratory tract (for example,
          swabs and aspirated fluid) or other parts of the body, including
          blood, serum, plasma, faeces, urine, cerebrospinal fluid (CSF), and
          tissues, for diagnostic purposes, detection of pathogens and further
          characterization, study or analysis or development of products.
        </li>
        <li>
          Benefits arising from BMEPP sharing will also entail rapid access to
          genetic sequence data (GSD) through one or more relevant, publicly
          accessible databases (e.g. European Nucleotide Archive, other
          databases in the International Nucleotide Sequence Databased
          Collaboration (INSDC), GISAID).
        </li>
      </ul>
      <br />
      <p id="P11">
        <b
          >11. What is the relationship with other labs/ surveillance
          networks?</b
        >
      </p>
      <ul>
        <li>
          The WHO BioHub System does not intend to replace or compete with
          existing systems, such as GISRS. It is intended to supplement the work
          of existing systems or networks
        </li>
        <li>
          The WHO BioHub is intended to cover emerging pathogens with pandemic
          potential - excluding influenza viruses which are covered by GISRS, or
          other pathogens that are covered by established networks
        </li>
        <li>
          Labs in other systems will be encouraged to engage, contribute to, or
          be part of the BioHub network
        </li>
        <li>
          For some labs, a key benefit of voluntarily engaging with the new
          system would be in the form of lab-specific capacity building
          activities including potential EQA schemes.
        </li>
      </ul>
      <br />
      <p id="P12">
        <b
          >12. What are the activities a lab would need to do in order to become
          a WHO BioHub Facility?</b
        >
      </p>
      <ul>
        <li>
          WHO has developed Terms of Reference that specify the activities that
          will be expected of participating labs.
        </li>
        <li>
          At a minimum, WHO BioHub Facilities will need to be able to receive,
          grow, sequence, and prepare BMEPP for distribution to other
          laboratories, subject to compliance and observance of applicable WHO
          BioHub System requirements, including those contained in relevant
          Standard Material Transfer Agreement(s) (SMTA(s)).
        </li>
        <ul>
          <li>
            Labs will need to be able to perform these activities safely and to
            the highest quality standards; they will need to have specific
            capacities and be able to operate in line with current, applicable
            national and international biosafety and biosecurity standards.
          </li>
        </ul>
      </ul>
      <br />
      <p id="P13">
        <b
          >13. What about a normal laboratory that just wants to provide or
          request biological material?</b
        >
      </p>
      <ul>
        <li>
          These laboratories would need to sign an SMTA in order to engage in
          the system and meet the necessary biosafety and biosecurity standards.
        </li>
      </ul>
