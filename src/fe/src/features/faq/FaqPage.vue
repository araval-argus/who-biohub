      <p id="P26"><b>26. Who will have access to these benefits?</b></p>
      <ul>
        <li>
          The WHO BioHub System is envisaged as a global public health system
          that would benefit all countries. It is therefore proposed that all
          countries have access to benefits generated from it, and where
          benefits are limited, that they be made available on the basis of
          public health need.
        </li>
      </ul>
