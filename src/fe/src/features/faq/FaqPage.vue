      <h3 id="C8">Incentives for participation</h3>
      <br />
      <p id="P27">
        <b>27. Why would a MS engage with the WHO BioHub System?</b>
      </p>
      <ul>
        <li>
          While this is a voluntary system there are many incentives and
          benefits that may arise for global public health and MS
        </li>
        <li>
          Global public health is advanced by shaping a system that encourages
          all countries to rapidly react to what could evolve into a pandemic
          situation, and accelerates response actions, including those that
          promote public health and social measures, and facilitate development
          of needed diagnostics and medical countermeasures
        </li>
        <li>
          All MS will benefit from access to Information and Risk Assessment,
          opportunities to advance Research through fostering international
          scientific projects with enhanced participation of scientists from
          developing countries, included proper acknowledgement in presentations
          and publications as well as engagement in Capacity Building activities
          such as training, mentoring and capacity strengthening
        </li>
      </ul>
      <br />
      <p id="P28">
        <b>28. Why would industry engage or want such a WHO BioHub System?</b>
      </p>
      <ul>
        <li>
          Industry would benefit from having legally clear, transparent,
          predictable and reliable pathways towards accessing BMEPP
        </li>
        <li>
          While different access-benefit sharing schemes could be developed,
          regardless of the mechanism, contributors (including industry) would
          also have clear visibility and impact of their contributions
        </li>
        <ul>
          <li>
            Ad-hoc mechanisms are generally inefficient and lack opportunities
            to test assumptions and conduct operational preparedness activities
            and exercises
          </li>
          <li>
            Establishing the ‘rules of the game’ in advance would eliminate
            hidden costs or situations that would lead to inequities in access
          </li>
        </ul>
      </ul>
