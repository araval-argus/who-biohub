      <h3 id="C1">Problem definition</h3>
      <br />
      <p id="P1">
        <b
          >1. What are the issues that the WHO BioHub System is trying to
          solve?</b
        >
      </p>
      <ul>
        <li>
          When a new pathogen emerges, there is no mechanism trusted by all MS
          to rapidly share materials in the very early stages of an outbreak.
        </li>
        <li>
          Existing initiatives to advance science and research are limited in
          geographic scope and rapidity.
        </li>
        <li>
          Sharing of pathogens needs to account for all applicable rules and
          regulations – including biosafety and biosecurity – and ensure an
          approach that maintains trust throughout for all MS and the public
        </li>
      </ul>
      <br />
      <p id="P2"><b>2. Why do we need a global system?</b></p>
      <ul>
        <li>
          A lesson that keeps emerging, especially with novel pathogens
          outbreaks and epidemics, is the need to rapidly and broadly share
          pathogens for effective surveillance and the timely development of
          diagnostics and other medical countermeasures.
        </li>
        <li>
          Currently there are different schemes that either involve already
          identified pathogens, or sharing is done bilaterally or on an ad hoc
          basis.
        </li>
        <li>
          So, there is no established, trusted global system that can kick
          immediately into gear and make sure that when a new pathogen emerges,
          countries can pro-actively contribute to public health, regardless of
          the nature of the pathogen.
        </li>
      </ul>
      <br />
      <p id="P3"><b>3. What is the end-goal/ solution?</b></p>
      <ul>
        <li>A trusted and transparent, global, end-to-end system that:</li>
        <ul>
          <li>
            that works across laboratories and partners to quickly characterize,
            sequence, and carry out necessary and risk assessments and other
            critical public health actions.
          </li>
          <li>
            works across countries and regions to facilitate the rapid
            development of diagnostics and medical countermeasures and their
            equitable availability to all countries based on public health
            needs.
          </li>
        </ul>
        <li>
          The System will be voluntary and will supplement existing WHO networks
          of laboratories and other related arrangements
        </li>
        <li>It will include one or more recognized WHO BioHub Facilities</li>
      </ul>
      <br />
      <p id="P4"><b>4. What are the objectives of the System?</b></p>
      <ul>
        <li>
          Promote rapid and timely sharing of biological materials with epidemic
          or pandemic potential;
        </li>
        <li>
          Facilitate rapid access to such pathogens and their information by
          relevant, interested, and qualified entities for the development of
          effective and safe public health products including diagnostics,
          vaccines and therapeutics; and
        </li>
        <li>
          Ensure fair and equitable access to such products by all countries,
          based on public health needs.
        </li>
      </ul>
      <br />
      <br />
      <h3 id="C2">Approach WHO is proposing to establish this System</h3>
      <br />
      <p id="P5"><b>5. What is the approach to reach the end-goal?</b></p>
      <ul>
        <li>It will consist of a phased approach:</li>
        <ul>
          <li>
            Phase I: ‘Proof-of-concept’/Piloting the WHO BioHub System (Mar. -
            Dec. 2021)
          </li>
          <ul>
            <li>
              limited to public health laboratories to advance public health
              activities and objectives (for non-commercial purposes)
            </li>
            <li>
              the first WHO BioHub Facility will be able to receive from Member
              States, on a voluntary basis, samples of SARS COV-2 variants of
              concern (e.g. 20I/501Y.V1, 20H/501Y.V2, 20J/501Y.V3, B.1.617).
            </li>
            <li>MoU for this set up signed on 24 May 2021.</li>
            <li>
              the first Facility will receive, store, grow, sequence, and
              distribute materials, as possible and subject to the availability
              of materials and funds and in accordance with standard Terms of
              Reference developed by WHO.
            </li>
          </ul>
          <li>
            Phase II: Expanding the WHO BioHub System to partners and other
            pathogens (Q1 2022)
          </li>
          <ul>
            <li>A broader System will be developed in Phase II.</li>
            <li>
              It will consist of sharing of both public health benefits (e.g.
              risk assessments and analyses) but add additional benefits such as
              medical countermeasures, as well as other products arising from
              access to WHO BioHub materials.
            </li>
            <li>
              Terms and conditions applicable to qualified entities that receive
              and use WHO BioHub materials and information will be developed for
              these non-commercial purposes
            </li>
            <li>
              In this stage the WHO BioHub will aim to strengthen partnerships
              by connecting with existing repositories or laboratory networks
              and gradually expanding its scope to other pathogens with epidemic
              or pandemic potential, allowing broader scientific research and
              growth into a more robust system.
            </li>
          </ul>
          <li>Phase III: as needed</li>
        </ul>
      </ul>
      <br />
      <p id="P6"><b>6. Why this phased approach?</b></p>
      <ul>
        <li>
          WHO is taking this approach to make sure that it is not only building
          a solution, but it is a robust, trustworthy venture, relying on
          standardised documentation. Therefore, the first phase is seen as an
          opportunity to apply best practices and use the SARS-COV-2 as a
          test-case. This does not override other important initiatives that
          exist or are being developed.
        </li>
        <li>
          While this is a voluntary endeavour, it is important to create
          momentum for a solution that will work equally for developing
          countries that are also concerned about scientific recognition and
          access and benefit considerations
        </li>
        <li>
          A Phased approach will enable WHO to consult with and gather input
          from MS and partners as it proceeds in a stepwise manner.
        </li>
      </ul>
