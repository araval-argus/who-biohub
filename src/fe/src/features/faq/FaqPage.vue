            outbreak investigations;
          </li>
          <li>
            Recommended risk management measures, including the use of
            therapeutic drugs and vaccines;
          </li>
          <li>
            Tools to aid countries in the performance of risk assessments;
          </li>
          <li>
            Updated laboratory protocols for the ongoing detection and
