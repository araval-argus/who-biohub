      <h3 id="C3">Research questions</h3>
      <br />
      <p id="P7">
        <b>7. What are the guiding principles and how are they used?</b>
      </p>
      <ul>
        <li>
          They are the ‘moral compass’ of the system, aimed to give direction to
          the operational design and implementation of the system
        </li>
        <li>
          The proposed guiding principles of the WHO BioHub System would
          include:
        </li>
        <ul>
          <li>A voluntary system for the global public health</li>
          <ul>
            <li>
              All contributions of BMEPP to the WHO BioHub will be entirely
              voluntary, based on the desire for rapid generation of information
              and other resources for global public health
            </li>
          </ul>
        </ul>
        <li>Timeliness</li>
        <ul>
          <li>
            To enable an effective public health response, the end-to-end system
            from sample collection to shipping and generation of scientific
            information must function with urgency. Data and analyses will be
            made publicly available in a timely manner, while respecting all
            applicable WHO, international, and national regulations and
            standards, and communicated promptly to decision-makers in the
            affected countries as well as more broadly to all WHO Member States
            to support effective and timely response measures.
          </li>
        </ul>
      </ul>

      <ul>
        <li>Equity and fairness</li>
        <ul>
          <li>
            Equity and fairness, as well as public health risk and need, will
            govern access to BMEPP contributed to the WHO BioHub System, and the
            research, data, and other materials resulting from the WHO BioHub
            System.
          </li>
        </ul>
      </ul>

      <ul>
        <li>Transparency</li>
        <ul>
          <li>
            Terms and conditions with respect to the use of BMEPP, sequence data
            and information from the WHO BioHub System will be made publicly
            available, as will criteria to receive BMEPP.
          </li>
        </ul>
      </ul>

      <ul>
        <li>Acknowledgement and co-authorship</li>
        <ul>
          <li>
            The contributions of collaborators to the WHO BioHub System,
            including laboratories providing BMEPP or genetic sequence data,
            will be appropriately acknowledged in presentations and
            publications, using guidelines such as those outlined by the
            International Committee of Medical Journal Editors. To the extent
            possible, entities using BMEPP in scientific research projects will
            seek the participation of scientists from the originating laboratory
            or countries and make efforts to engage them in preparation of
            manuscripts for presentation and publication.
          </li>
        </ul>
      </ul>

      <ul>
        <li>Sustainability and maximal preservation</li>
        <ul>
          <li>
            The BMEPP and associated data (e.g epidemiological information)
            available through the WHO BioHub System will be critical for
            understanding diseases with epidemic and pandemic potential and
            developing tools to combat them. These important resources will need
            to be maintained and managed over the longer term. The WHO BioHub
            System will therefore be established and managed with longer term
            sustainability and maximal preservation in mind.
          </li>
        </ul>
      </ul>

      <ul>
        <li>Collaboration & Cooperation</li>
        <ul>
          <li>
            The WHO BioHub System will promote collaboration and cooperation
            with existing networks, repositories, and scientific groups to
            strengthen knowledge and contribute to the advancement of effective,
            efficient, fair and equitable response to epidemic or pandemic
            public health events.
          </li>
        </ul>
      </ul>

      <ul>
        <li>Best practices for safety and security</li>
        <ul>
          <li>
            The WHO BioHub System will follow procedures that ensure that BMEPP
            which are shared have been properly characterized, usually through
            culture and sequencing for pathogen materials. They will be
            prepared, dispatched, received, processed, stored and shipped to
            qualified recipients according to current, applicable national and
            international biosafety and biosecurity standards.
          </li>
        </ul>
      </ul>

      <ul>
        <li>Consistency with applicable law</li>
        <ul>
          <li>
            The WHO BioHub System will be established and operated in a manner
            consistent with applicable law, regulations, rules, and standards,
            including under national and international law.
          </li>
        </ul>
      </ul>

      <ul>
        <li>
          Consistency with applicable ethical regulations, norms, and standards
          requirements
        </li>
        <ul>
          <li>
            The WHO BioHub System will be established and operated in a manner
            consistent with applicable WHO, international, and national ethical
            regulations, norms, and standards.
          </li>
        </ul>
      </ul>
