      <h3 id="C6">Legal and governance questions</h3>
      <br />
      <p id="P16">
        <b
          >16. What is the relation between the BioHub and the Nagoya
          Protocol?</b
        >
      </p>
      <ul>
        <li>
          The question overall is a decision and matter that rests with the
          relevant Nagoya party or parties in terms of any normative linkage.
        </li>
        <li>
          Obviously, there is a very significant public health aspect to the
          Nagoya Protocol, and indeed the World Health Assembly is seized with
          public health considerations related to the Nagoya Protocol,
          including, for example, World Health Assembly Decisions WHA 72(12) and
          72(13).
        </li>
        <li>
          Accordingly, the Secretariat will continue to report under those
          resolutions as well as others on the public health implications of
          these matters.
        </li>
        <li>
          That said, a broad objective would be to develop BioHub mechanisms
          that are consistent with the growing current of national and
          international access and benefit sharing principles and goals, notably
          those set out in the Nagoya Protocol, so that the BioHub could
          potentially be considered in relation to Nagoya.
        </li>
      </ul>
      <br />
      <p id="P17">
        <b
          >17. What is the basis for the WHO’s Secretariat’s commencing this
          project?</b
        >
      </p>
      <ul>
        <li>
          At this early stage, the WHO BioHub is a pilot project, aimed to test
          proof-of-concept.
        </li>
        <li>
          The Secretariat looks to MS to advise how to move forward with the
          project and how to integrate its further development and full
          operationalization into appropriate WHO arrangements.
        </li>
      </ul>
      <br />
      <p id="P18">
        <b>18. What are the legal considerations and the rules of sharing?</b>
      </p>
      <ul>
        <li>
          Standard Material Transfer Agreements (SMTAs) will establish the terms
          and conditions for sharing biological materials with epidemic or
          pandemic potential into the WHO BioHub System and to entities outside
          the WHO BioHub System.
        </li>
        <li>
          Member States’ input on this will be sought as part of consultations
          in the further development of the BioHub System.
        </li>
      </ul>
      <br />
      <p id="P19"><b>19. Will WHO share all the model SMTAs?</b></p>
      <ul>
        <li>
          Yes – it is intended that the model SMTAs will be publicly available
          on WHO’s website in line with normal practices (e.g. PIP).
        </li>
      </ul>
      <br />
      <p id="P20">
        <b
          >20. What is WHO’s approach when a lab has obtained a sample from
          another lab in the same country? Can the lab that obtained the samples
          from the other lab, share them with the BioHub under a BioHub SMTA?</b
        >
      </p>
      <ul>
        <li>
          Yes, it is the responsibility of the sending lab to ensure they have
          the permissions to onward share the samples (regardless of whether
          they obtained it from another lab in their country). The sending lab
          can specify any restrictions on the analysis to be performed in the
          Annex 2 (in accordance with any internal agreements and
          considerations).
        </li>
      </ul>
      <br />
      <p id="P21">
        <b
          >21. What is the relationship with the Pandemic Preparedness and
          Response Treaty that is under discussion?</b
        >
      </p>
      <ul>
        <li>
          It is expected that the treaty would establish principles, priorities
          and targets for pandemic preparedness and response through a
          legally-binding international framework.
        </li>
        <li>
          WHO initiatives relevant to the treaty, including those currently
          under development and consideration, such as the WHO BioHub and the
          Universal Health and Preparedness Review, may become part of the
          treaty, if, and only if, so decided by Member States.
        </li>
        <li>
          It is however too early, at this time, to indicate whether, how and to
          what extent that might occur. In any event, it is expected that the
          key issues that these initiatives intend to address and the gaps they
          are intended to close would be addressed in the treaty.
        </li>
      </ul>
      <br />
      <p id="P22">
        <b
          >22. How are Intellectual Property Rights (IPR) rights proposed to be
          handled in the System?</b
        >
      </p>
      <ul>
        <li>
          The WHO BioHub System will be mindful of the matter of intellectual
          property rights (IPR) and the need to protect pre-existing rights. In
          developing an approach to IPRs for the WHO BioHub System, WHO intends
          to take into consideration prior work on the matter, including that
          done in the context of PIP and the WHO R & D BluePrint.
        </li>
      </ul>
      <br />
      <br />
      <h3 id="C7">Access and benefit sharing?</h3>
      <br />
      <p id="P23">
        <b>23. Who will have access to the WHO BioHub System materials?</b>
      </p>
      <ul>
        <li>
          It is proposed that BMEPP would be made available to “Qualified
          Entities” as follows:
        </li>
        <li>Qualified entities will have access to the WHO BioHub System.</li>
        <ul>
          <li>These may be categorized as follows:</li>
          <ul>
            <ol type="i">
              <li>
                entities that use BMEPP for non-commercial public health
                purposes only.
              </li>
              <li>
                entities that will use BMEPP for all purposes including
                commercial purposes.
              </li>
            </ol>
          </ul>

          <li>
            Any Qualified Entity wishing to receive BMEPP will be required to:
          </li>
          <ul>
            <li>
              Meet applicable WHO, international and national biosafety and
              biosecurity standards (see Question 15 above); and
            </li>
            <li>Conclude an agreement with WHO, as follows:</li>
            <ul>
              <li>
                Where the Qualified Entity is a WHO Member State or an entity
                requesting BMEPP for non-commercial public health purposes only,
                it will conclude a WHO BioHub System Standard Material Transfer
                Agreement 2 (SMTA) with the WHO BioHub Facility that provides
                the BMEPP and WHO.
              </li>
              <li>
                Where the Qualified Entity is an entity requesting use of the
                BMEPP for all purposes including commercial purposes, it will
                conclude a WHO BioHub System SMTA 3 with WHO.
              </li>
            </ul>
          </ul>
        </ul>
      </ul>
      <br />
      <p id="P24">
        <b
          >24. Will there be limitations to the use of BMEPP by (Qualified
          Entities)</b
        >
      </p>
      <ul>
        <li>
          In the first Phase, the focus will be on sharing for non-commercial
          public health purposes only; this would include: surveillance
          activities, diagnostic applications, and quality assurance.
        </li>
        <li>
          Future sharing provisions would include for commercial purposes and
          will be covered in a separate SMTA that still needs to be developed
        </li>
      </ul>
      <br />
      <p id="P25">
        <b>25. What are the anticipated benefits arising from the System?</b>
      </p>
      <ul>
        <li>The benefits vary and would include the following categories:</li>
        <ul>
          <li>Information and Risk Assessment:</li>
          <ul>
            <li>
              Public health information and tools, (including regular reports,
              risk assessments and related tools, guidelines and lab protocols)
            </li>
            <li>
              Rapid access to GSD through relevant publicly accessible databases
            </li>
          </ul>
        </ul>

        <ul>
          <li>Public Health Goods</li>
          <ul>
            <li>
              Development and access to countermeasures (vaccine, therapeutics,
              diagnostics and so on)
            </li>
            <li>
              Timely updated and evidence-based recommendations on
              countermeasures
            </li>
          </ul>
          <li>Capacity Building:</li>
          <ul>
            <li>Training, mentoring and capacity strengthening</li>
          </ul>
          <li>Research</li>
          <ul>
            <li>
              Fostering international scientific projects with enhanced
              participation of scientists, from developing countries included
              proper acknowledgement in presentations and publications
            </li>
          </ul>
        </ul>
      </ul>

      <b>OR:</b>
      <ol class="custom-marker parens-after decimal">
        <li>
          Fair and equitable access to medical and non-medical countermeasures
          or products that may be developed to address outbreaks of BMEPP. Such
