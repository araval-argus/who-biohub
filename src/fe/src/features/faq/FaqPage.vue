      <h3 id="C10">Operational questions</h3>
      <p id="P32">
        <b
          >32. Who will take care of the global transport and related
          coordination?</b
        >
      </p>
      <ul>
        <li>Transport/logistics management will be coordinated at HQ level.</li>
        <li>
          Global related transport SOPs will be developed in coordination with
          shipping agents.
        </li>
      </ul>
      <br />
      <p id="P33">
        <b
          >33. How will you keep track of shipments and materials in the WHO
          BioHub Facility?</b
        >
      </p>
      <ul>
        <li>WHO will develop a system to ensure tracking of BMEPP.</li>
        <li>
          This will build on best practices from influenza and yellow fever with
          a view to facilitate monitoring and evaluation activities.
        </li>
        <li>
          The system is to be developed but some basic KPIs that are envisaged
          to be monitored are:
          <ul>
            <li>Days between request and pickup of samples</li>
            <li>Days between pickup and delivery of samples</li>
            <li>Total transport days of samples</li>
            <li>Financial spending</li>
            <li>Tracking of movements</li>
          </ul>
        </li>
      </ul>
      <br />
      <p id="P34">
        <b
          >34. How long will it take to send or obtain a sample from the WHO
          BioHub Facility?</b
        >
      </p>
      <ul>
        <li>
          The aim of the system is to make the sample sharing process as fast as
          possible. While precise timings depend on certain constraints, KPIs
          will be established in due time being mindful of experiences from
          other sample sharing endeavors.
        </li>
      </ul>
