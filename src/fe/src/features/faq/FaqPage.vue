      <h3 id="C9">Management</h3>
      <br />
      <p id="P29">
        <b
          >29. What is the organization of the work? What is the management
          approach and the applicable SOPs?</b
        >
      </p>
      <ul>
        <li>
          The project is structured around two workstreams and two phases. This
          approach is proposed as the immediate activities are thought to
          provide lessons on proof of concept and operational progress for
          sharing in view of non-commercial public health use.
        </li>
        <li>
          At HQ, WHO has repurposed, an inclusive and crosscutting team of
          dedicated experts with selected expertise spanning:
        </li>
        <ul>
          <li>Epidemics and pandemic preparedness and response</li>
          <li>
            Developing, coordinating and leading global laboratory networks and
            global sharing of viruses
          </li>
          <li>
            Legal expertise, with a long track record in global benefit-sharing
            agreements and complex international negotiations
          </li>
          <li>
            Operationalisation of benefit-sharing agreements and related
            governance frameworks
          </li>
          <li>Biosafety and Biosecurity</li>
          <li>Global logistics management</li>
        </ul>
        <li>
          WHO will also involve regional offices as the project progresses and
          will strive that many if not all regions will be engaged early on
        </li>
        <li>
          When considering work with participating labs and other partners, work
          is starting towards developing corresponding SOPs. These will build on
          best practices that WHO is registering from a variety of projects –
          including influenza, yellow fever, COVID and others. The SOPs will
          likely address the receipt, processing, characterization (by a variety
          of means, including genetic sequencing), amplification in culture,
          aliquoting, labelling, cryopreservation (and, as necessary, validation
          of the concentration, purity, and other characteristics of the
          cryopreserved product), and forward shipping of that product. The
          shipping SOPs will have to be aligned with the shipping company – WHO
          already has such documentation and will work with partners to adapt it
          for this project.
        </li>
      </ul>
      <br />
      <p id="P30">
        <b>30. What are the envisaged governance structures for the System?</b>
      </p>
      <ul>
        <li>These are to be developed in consultation with MS.</li>
        <li>
          As with most WHO projects, we envision the need for a WHO Secretariat
          that will represent a technical level, responsible for day to day
          running of operations as well as performing the necessary analysis and
          preparations to facilitate System design activities. It has functions
          pertaining to coordination, organization, communication, data
          collection (to enable decision-making as well as monitoring and
          evaluation) and reporting.
        </li>
        <li>
          Currently the project has been benefiting from a WHO Senior Steering
          Committee formed by the WHO DG, WHE EXD and WHO Chief Scientist.
        </li>
        <li>
          A foreseeable Advisory/ Decision-making body may be needed in case
          demand outstrips supply when it comes to sample sharing – meaning
          there is a large number of request to obtain samples from the WHO
          BioHub Facility which would exceed the pace that the Facility can
          serve demands. This body would be asked to advise on prioritization of
          requests in such situations with the hope that as time goes by all
          requests by qualified entities could be met as soon as possible.
        </li>
      </ul>
      <br />
      <p id="P31"><b>31. Who will take care of the costs?</b></p>
      <ul>
        <li>
          In a first phase the major costs are associated with international
          transport of samples. To facilitate a rapid operationalization and the
          need for coordination with the Swiss Lab, and the fact that the first
          phase will focus on COVID-19 sample sharing, funding that was
          fundraised for this response will be disbursed appropriately.
        </li>
        <li>
          It is to be determined how qualified entities may support the future
          running of the project.
        </li>
        <li>
          WHO is grateful to Switzerland for agreeing to support the costs
          associated with the WHO BioHub Facility located at Spiez Laboratory
        </li>
        .
      </ul>
